    testEncryptDecrypt: function(){
        var cipher = SECCipher.initWithAlgorithm(SECCipher.Algorithm.rsaOAEP);
        var expectation = TKExpectation.init();
        expectation.call(cipher.createKey, cipher, function(privateKey){
            TKAssertNotNull(privateKey);
            expectation.call(privateKey.getJWK, privateKey, function(privateJWK){
                TKAssertNotNull(privateJWK);
                expectation.call(privateKey.publicKey.getJWK, privateKey.publicKey, function(publicJWK){
                    TKAssertNotNull(publicJWK);
                    var jwe = SECJSONWebEncryption.init();
                    expectation.call(jwe.encrypt, jwe, "Testing 123".utf8(), publicJWK, function(jweString){
                        TKAssertNotNull(jweString);
                        var jwe2 = SECJSONWebEncryption.initWithString(jweString);
                        expectation.call(jwe2.decrypt, jwe2, privateJWK, function(plaintext){
                            TKAssertNotNull(plaintext);
                            TKAssertExactEquals(plaintext.stringByDecodingUTF8(), "Testing 123");
                        });
                    });
                });
            });
        });
        this.wait(expectation, 1.0);
    }

